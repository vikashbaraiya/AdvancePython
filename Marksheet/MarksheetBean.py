class markSheetBean():
    rollNo = int
    name  = ""
    physics = int
    chemistry = int
    maths  = int

    def setName(self,name):
        self.name = name

    def getName(self):
        return self.name

    def setRollNo(self, rollNo):
        self.rollNo = rollNo

    def getRollNo(self):
        return self.rollNo

    def setPhysics(self, physics):
        self.physics = physics

    def getPhysics(self):
        return self.physics

    def setChemistry(self, chemistry):
        self.chemistry = chemistry

    def getChemistry(self):
        return  self.chemistry

    def setMaths(self,maths):
        self.maths = maths

    def getMaths(self):
        return self.maths




